package InteviewquestionsPractice;

public class Swap2Numswithoutusing3rdvariable {
    public static void main(String[] args) {
        int a=5,b=6;

        a=a+b;//5+6->a=11
        b=a-b;//11-6->b==5
        a=a-b;//11-5;a=5

        System.out.println(a);
        System.out.println(b);
    }
}
