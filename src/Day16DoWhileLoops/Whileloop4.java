package Day16DoWhileLoops;

public class Whileloop4 {
    public static void main(String[] args) {
        while (true){
            System.out.println("hello");
            break;
        }


        int i =0;

        while (true){
            i++;
            System.out.println("Hello "+i);
            if (i==5){
                break; }
        }
    }
}
