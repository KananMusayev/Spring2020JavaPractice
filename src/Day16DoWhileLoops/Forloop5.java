package Day16DoWhileLoops;

public class Forloop5 {
    public static void main(String[] args) {

        /*
        use for loop to print all the even number between 1~100
         */

        for (int i=1;i<100;i++){

            if (i%2==0){

                System.out.println(i);
            }
        }


    }
}
