package ClassTasks;

public class Warmup15 {
    public static void main(String[] args) {
        /*
        use while loop
        and print all the even nums between 1-100
         */
        int i=1;

        while (i<=100){
            i++;

            if (i%2==0){
                System.out.println(i);
            } }
    }
}
