public class Rephlit144 {
    public static void main(String[] args) {
sign(5);

    }


    public static void sign(int n) {

        if (n>0){
            System.out.println("1");
        }
        else if (n<0){
            System.out.println("-1");
        }
        else if (n==0){
            System.out.println("0");
        }
    }


}
